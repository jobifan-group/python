print("We are going to print numbers less than 10")
for num in range(10):
    print(num, end=" ")

print("\n")

for num in range(0, 10):
    print(num, end=" ")

print("\nWe are going to print even numbers from 10 to 20")
for num in range(10, 21, 2):
    print(num, end=" ")

print("\nWe are going to print odd numbers from 11 to 21")
for num in range(11, 22, 2):
    print(num, end=" ")

print("\nWe are going to print numbers from 11 to 21, by step 5")
for num in range(11, 22, 5):
    print(num, end=" ")